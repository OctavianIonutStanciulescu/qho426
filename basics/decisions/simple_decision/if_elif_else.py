#Using the if and elif function for the first time
direction = input("Towards which direction should I paint (up, down, left or right?\n")
if direction == "up":
  print("\nI am painting in the upward direction!")
elif direction == "down":
  print("\nI am painting in the downward direction!")
elif direction == "left":
  print("\nI am painting in the left direction!")
elif direction == "right":
  print("\nI am painting in the right direction!")
  